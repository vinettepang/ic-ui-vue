                                <p class="bk-question">Q1:Q1:产品经理怎么入门，如何自主学习？</p>
                                <div class="bk-answer  answer-bottom over-answer">
                                    <div class="answer-cover"></div>
                                    <p>1.首先清楚产品经理的要求是什么？</p>
                                    <p>想要成为一个产品经理，总要先了解企业对产品经理的要求是什么，大部分公司要求的产品经理即功能型产品经理：①设计技能：懂得产品模式的功能结构，掌握功能的策划和设计；②产品软件：熟练使用office、思维导图、原型设计软件、各类产品文档；③产品模式：了解B2C、O2O、SNS等产品的功能结构和运作模式。</p>
                                    <p>2.和产品经理的差距在哪里？如何提高？</p>
                                    <p>从最简单的软件入手。产品相关软件：①使用Axure进行原型设计 ②使用Mindmanager、Xmind绘制思维导图 ③Axure，Visio画流程图 ④项目管理软件：Project，禅道； ⑤办公软件：Office，WPS等；以上软件起码要掌握基础的操作，如利用AXURE会做简单的交互动作，利用VISIO会画简单的流程图，泳道图；利用Peoject会做简单的项目排期，利用Excel会做简单的数据分析。</p>
                                    <p>清楚不同产品模式下不同的功能结构，并且可以根据企业的产品模式，设计出产品原型及文档；这里的建议是： ①从设计开始：对不同产品模式的网站：如B2B,B2C,C2C这三种不同类型的网站，尝试着自己设计出一套产品原型出来，从最简单的一套流程开始，如注册流程、下单流程... ②对比：把自己设计的产品，与真正的网站进行对比，找出自己所完成的流程与真正的、成熟的流程相比下的差距在哪里 ③小结：思考以下这些问题：有哪些细节没有想到？为什么会有这样的差距？自己的思维有哪些盲点？设计这些产品的产品经理是怎么考虑的？ ④反复修正：根据对比的结果找到自己的不足后，给第一次自己做的设计进行修改，再重新进行对比——小结——修改，对作品进行反复的修正。 ⑤如果掌握了相关软件和不同产品模式下的功能设计，那么当个产品专员是足够了的。</p>
                                    <p>3.产品经理必备的素质：</p>
                                    <p>①一定的技术功底或者知识：虽然在知乎上大家一直都在讨论产品经理到底需不需要技术功底，但是掌握一定的编程知识，不但可提升自己的逻辑思维能力，在跟程序员的沟通上，也少了很多问题，所以就算不会编程，还是建议掌握一定的技术知识。 ②一定的运营知识：再好的产品也需要运营，不管是为了产品还是为了更好的同运营部门沟通，运营知识是少不了的； ③一定的沟通技巧：产品经理虽然被称为迷你CEO，但他是一个没有权力的CEO，如何利用公司的各种资源为自己服务，完成产品，那么沟通技巧就不可缺少了； ④一定的设计技巧：在很多公司没有交互设计职位的情况下，产品经理和UI负责了整个产品的设计，不具备一定设计知识的无法把产品经理的工作做到游刃有余。如果以上的标准都达标，那么获得一个产品经理的职位，并不是很困难的事情。</p>
                                    <p>总结起来，产品经理最重要的三条能分别是学习能力、逻辑思维、沟通。而其中，学习能力排在第一位。知乎上有大量关于产品方面的提问和答案，在书店中也有大量关于产品经理的书籍，只要你有心去学习、去关注，掌握相关知识和技能并不是一件什么难事。</p>
